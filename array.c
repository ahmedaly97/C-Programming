/*******************************************************************
**	 last occurance in array  function : A function that          **
**		takes:                                                    **
**			1- Input array  2-size of array     		       	  **
**		returns:                                                  **
**			 the index  of last occurance in array elements	      **
********************************************************************/


int last_occurance_arr(int * arr , int size)
{
	int i, c=0;

	for(i=0 ; i< size ; i++)
	{
		
		if ( arr[i+1] == arr[i] )
		{
			i++;
			while (arr[i+1] == arr[i])
			{
				c++;
			}
		}
	}
	return c+i ; 
	
}



