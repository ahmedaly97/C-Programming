/*******************************************************************
**	 swap two arrays  function : A function that 		          **
**		takes:                                                    **
**			1- Input array1  2-size of array 3- same size of both **
**		returns:                                                  **
**			 the both arrays swapped							  **
********************************************************************/



void swap_array(int * arr1 , int * arr2 , int size )
{
	
	int i;
	for(i=0 ; i< size ; i++)
	{
		
		swap (&arr1[i] ,& arr2[i]);
		
	}
	
}
