/*******************************************************************
**	 first occurance in array  function : A function that         **
**		takes:                                                    **
**			1- Input array  2-size of array     		       	  **
**		returns:                                                  **
**			 the index  of first occurance in array elements	  **
********************************************************************/


int first_occurance_arr(int * arr , int size)
{
	int i;

	for(i=0 ; i< size ; i++)
	{
		
		if ( arr[i+1] == arr[i] )
		{
			break ;
		}
	}
	return i ; 
	
	
	
	
}

